package nyc.c4q.washingtonpostjson.backend;

public class Post {
    private String id;
    private String title;
    private String content;
    private String excerpt;
    private String date;

    public String getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getContent() {
        return content;
    }

    public String getExcerpt() {
        return excerpt;
    }

    public String getDate() {
        return date;
    }


}
